# Tipuri de date in R

a <- 2
print(a)
print(class(a))

# numeric - echivalent float din python
a <- 2.3
print(a)
print(class(a))

# integer - echivalent int din python (se adauga L la final)
b <- 19L
print(b)
print(class(b))

# logical - echivalent boolean din python
c <- TRUE
print(c)
print(class(c))

# logical - echivalent boolean din python
d <- FALSE
print(d)
print(class(d))

# character - echivalent str din python
# se folosesc ghilimele duble sau simple
e <- "telefon"
print(e)
print(class(e))

e <- 'telefon'
print(e)
